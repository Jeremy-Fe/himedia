package Guro_04_27;

public class OperatorExample_p70 {

	public static void main(String[] args) {
		int a = 10;
        int b = 0;
        b = ~a;
        
        System.out.println(a);
        System.out.println(b);
        
        b = ~b;
        
        System.out.println(b);

	}

}
